/**
 * op_add - will add two integers
 * @a: first integer
 * @b: second integer
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - return difference between two integer
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - return result of divide two number
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return remider of division of two integer
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
